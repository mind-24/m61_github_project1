# โปรแกรมคำนวณค่าไฟฟ้า

print("==============================")
print("     โปรแกรมคำนวณค่าไฟฟ้า")
print("==============================")

# รับจำนวนหน่วยไฟฟ้า
unit = float(input("กรุณากรอกจำนวนหน่วยไฟฟ้า: "))

# คำนวณค่าไฟ
electricity_bill = unit * 4.50

# แสดงผลลัพธ์ 2 ตำแหน่ง
print("\n===== ผลการคำนวณ =====")
print(f"จำนวนหน่วยไฟฟ้า: {unit:.2f} หน่วย")
print(f"ค่าไฟฟ้าทั้งหมด: {electricity_bill:.2f} บาท")